void randomlyChoose(int arr[], int l, int r) {
    int pos = l + rand() % (r - l);
    assert(pos <= r and pos >= l);
    swap(arr[pos], arr[r]);
}

int partition(int arr[], int l, int r, int pi) {
    int pivot = arr[r];
    int i = l - 1;
    for (int j = l; j < r; j++) {
        if (arr[j] < pivot) {
            i++;
            swap(arr[i], arr[j]);
        }
    }
    swap(arr[i+1], arr[r]);
    return i+1;
}

void QuickSort(int arr[], int l, int r) {
    if (l >= r) return;
    randomlyChoose(arr, l, r);
    int p = partition(arr, l, r, r);
    QuickSort(arr, l, p-1);
    QuickSort(arr, p+1, r);
}


