for _test_ in range(int(input())): 
	n, k = get_ints()
	arr = get_list()
	d = defaultdict(list)
	ans = [0 for x in range(n)] 
	for i in range(n):
		d[arr[i]].append(i)

	for ke, va in sorted(d.items()):
		if ke >= (k + 1)// 2:
			break
		if (k - ke) in d:
			sz1 = len(va)
			sz2 = len(d[k-ke])
			# print(ke, sz1, sz2)
			if sz1 < sz2:
				for j in range(sz1):
					ans[va[j]] = 1
			else:
				reffe = d[k-ke]
				for j in range(sz2):
					ans[reffe[j]] = 1
	if not (k & 1):
		if k // 2 in d:
			sz = len(d[k//2])
			for j in range(sz // 2):
				ans[d[k//2][j]] = 1
	print(*ans)






