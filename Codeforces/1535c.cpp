class Solution {
    static const int maxN = int(1e5) + 2;
public:
    Solution() {
    }
public:
    void SolveEachTest(int _TestCase) {
        // cout << "Case #" << _TestCase << ":";    
        string s; cin >> s;
        lld n = len(s);
        lld ans = 0ll;
        lld ind = 0, prevQs = 0ll, sz = 0ll, st = 1;
        while (ind < n) {
            ans -= getCnt(prevQs);
            while (ind < n) {
                if (st == 0 and s[ind] != '?' and s[ind] == s[ind-1]) {
                    break;
                } else {
                    st = 0;
                    if (s[ind] == '?') {
                        prevQs++;
                        if (ind > 0 and s[ind-1] == '1') {
                            s[ind] = '0';
                        } else if (ind > 0 and s[ind-1] == '0') {
                            s[ind] = '1';
                        }
                    } else {
                        prevQs = 0ll;
                    }
                    ind++, sz++;
                }
            }
            // _____error_____(sz, s, ind);
            ans += getCnt(sz);
            sz = prevQs;
            st = 1;
        }   
        cout << ans;
    }

public:
    void ResetTestCase() {
        cout << "\n";
    }

    lld getCnt(lld sz) {
        return (sz * (sz+1)) / 2;
    }
};



